    public class Gracz {
    private String nazwa;
    private char oznaczenie;




    public Gracz(String nazwa, char oznaczenie) {
        this.nazwa = nazwa;
        this.oznaczenie = oznaczenie;
    }

    public String getNazwa() {
        return nazwa;
    }

    public char  getOznaczenie() {
        return oznaczenie;
    }
}



